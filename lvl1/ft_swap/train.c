void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

int		main(void)
{
	int x = 10;
	int y = 20;
	ft_swap(&x, &y);
	printf("%d,%d",x,y);
	return (0);
}
